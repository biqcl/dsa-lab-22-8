#!/bin/bash

# Проверяем, передан ли аргумент
if [ $# -eq 0 ]; then
    echo "Ошибка: Не указана директория" >&2
    echo "Использование: $0 <директория>" >&2
    exit 1
fi

directory="$1"

# Проверяем наличие директории
if [ ! -d "$directory" ]; then
    echo "Ошибка: Директория '$directory' не существует" >&2
    exit 1
fi

# Выводим файлы, отсортированные по дате изменения
ls -lt "$directory" | grep -v '^total'
