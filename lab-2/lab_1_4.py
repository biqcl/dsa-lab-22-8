# Считываем последовательность чисел с клавиатуры
print("Введите последовательность целых чисел, разделенных пробелами:")
sequence = input().strip()  # Убираем лишние пробелы в начале и конце

# Инициализируем переменные
sum_n = 0  # Сумма чисел
count_n = 0  # Количество чисел
i = 0  # Индекс для перемещения по строке

# Обрабатываем строку с помощью цикла while
while i < len(sequence):
    # Пропускаем пробелы
    while i < len(sequence) and sequence[i] == ' ':
        i += 1

    # Собираем число (включая отрицательные)
    n = ""
    while i < len(sequence) and (sequence[i].isdigit() or (sequence[i] == '-' and n == "")):
        n += sequence[i]
        i += 1

    # Добавляем его к сумме и увеличиваем счетчик
    if n:  # Проверяем, что число не пустое
        sum_n += int(n)
        count_n += 1

# Выводим результаты
print(f"Сумма всех чисел: {sum_n}")
print(f"Количество всех чисел: {count_n}")

